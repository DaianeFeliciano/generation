package heranca;

public class Animal {
	
	String nome;
	String emiteSom;
	int idade;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getEmiteSom() {
		return emiteSom;
	}
	public void setEmiteSom(String emiteSom) {
		this.emiteSom = emiteSom;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	
	
}
